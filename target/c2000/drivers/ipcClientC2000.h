/*
 * @file ipcClientC2000.h
 *
 * @brief Client for the inter-processor communication (IPC) library.
 *
 */

#ifndef IPC_CLIENT_C2000_H_
#define IPC_CLIENT_C2000_H_

//=============================================================================
/*-------------------------------- Includes ---------------------------------*/
//=============================================================================
#include "stdint.h"
#include "stddef.h"

//=============================================================================

//=============================================================================
/*------------------------------- Definitions -------------------------------*/
//=============================================================================
/* CPU1->CPU0 timeout error code */
#define IPC_CLIENT_C2000_ERR_CPU1_REPLY_TO				-1

/* Invalid CPU1 command */
#define IPC_CLIENT_C2000_ERR_CPU1_INVALID_CMD			-2

/* Data received exceeds CPU0->CPU1 buffer */
#define IPC_CLIENT_C2000_ERR_CPU0_CPU1_BUFFER_OVERFLOW	-3

/* Lock error code */
#define IPC_CLIENT_C2000_ERR_LOCK_TO                     -4
//=============================================================================

//=============================================================================
/*-------------------------------- Functions --------------------------------*/
//=============================================================================
//-----------------------------------------------------------------------------
void ipcClientC2000Initialize(void *param);
//-----------------------------------------------------------------------------
int32_t ipcClientC2000IrqSend(void);
//-----------------------------------------------------------------------------
int32_t ipcClientC2000IrqReceive(uint32_t timeout);
//-----------------------------------------------------------------------------
int32_t ipcClientC2000Lock(uint32_t timeout);
//-----------------------------------------------------------------------------
void ipcClientC2000Unlock(void);
//-----------------------------------------------------------------------------
//=============================================================================
#endif /* IPC_CLIENT_C2000_H_ */
